class ЭлектронныйКомпонент:
    """Базовый класс для всех электронных компонентов."""

    def __init__(self, название: str):
        self._название = название

    @property
    def название(self) -> str:
        return self._название

    @название.setter
    def название(self, значение: str):
        self._название = значение


class Резистор(ЭлектронныйКомпонент):
    def __init__(self, название: str, сопротивление_ом: float,
                 мощность_вт: float, точность_процент: float):
        super().__init__(название)
        self._сопротивление_ом = сопротивление_ом
        self._мощность_вт = мощность_вт
        self._точность_процент = точность_процент

    @property
    def сопротивление_ом(self) -> float:
        return self._сопротивление_ом

    @сопротивление_ом.setter
    def сопротивление_ом(self, значение: float):
        self._сопротивление_ом = значение

    @property
    def мощность_вт(self) -> float:
        return self._мощность_вт

    @мощность_вт.setter
    def мощность_вт(self, значение: float):
        self._мощность_вт = значение

    @property
    def точность_процент(self) -> float:
        return self._точность_процент

    @точность_процент.setter
    def точность_процент(self, значение: float):
        self._точность_процент = значение

    def __repr__(self):
        return (f"Резистор({self.название!r}, "
                f"{self.сопротивление_ом} Ом, "
                f"{self.мощность_вт} Вт, "
                f"±{self.точность_процент}%)")


